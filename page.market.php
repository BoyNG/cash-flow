2-й уровень Вы получаете уже у спонсора 2-го уровня (который стоит над спонсором 1-го уровня), 
далее 3-й уровень у спонсора 3-го уровня, аналогично 4-й и 5-й уровни у спонсоров 4-го и 5-го уровня.<br>
