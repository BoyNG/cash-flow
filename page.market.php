Для того, чтобы начать получать денежные подарки в нашем проекте, 
Вам необходимо получить первый уровень у вышестоящего участника
(у спонсора 1-го уровня, как показано на рисунке). 
После того, как он Вас подтвердит, Вы можете начинать приглашать людей уже в свою структуру.<br>
