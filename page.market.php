В более быстром заполнении линий рефералами Вам поможет наша система переливов от системы и вышестоящих участников.
<br>
Представим себе следующую ситуацию, которая показана на картинке.
Белые - это активные рефералы (1, 2, 3, 4, 5, 7, 10 и 11). Серые - это пустые места, ещё не занятые рефералами (6, 8, 9 и 12).
			<br>
			<img class="center-img" src='img/market3.jpg' alt='marketing'>
			<br>
Вы пригласили рефералов 1, 2 и 3. Затем они пригласили рефералов 4, 5, 7, 10 и 11. 
Ваша первая линия заполнена полностью, так как в ней уже есть 3 человека. 
Когда Вы пригласите 4-го реферала, то он пойдет не к Вам в первую линию, 
а в первую линию одного из Ваших рефералов первой линии. 
В нашем примере он встанет под участника 1 (6-ой на картинке) и получит первый уровень у него, тем самым станет потенциальным получателем 2-го уровня от Вас.<br>
			<br>
Точно такая же схема и для участников 8, 9 и 12.<br>
			<br>
А когда вторая линия тоже заполнится, то Ваш реферал уйдет переливом уже в третью линию и будет потенциальным получателем 3-го уровня тоже от Вас.<br>
И так далее вниз по уровням.
