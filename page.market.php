Рассмотрим следующую ситуацию. 
У Вас в первой линии ещё есть одно свободное место и на него предендуют сразу 3 реферала (A, B, C). 
У всех их система предложит получить 1-й уровень у Вас. 
Даже если они все трое отправят Вам заявку, 
то они все равно не смогут занять место в Вашем реферальном дереве до тех пор, 
пока Вы их не подтвердите. Первым займет свое место тот (место 3, как на рисунке), 
кого Вы подтвердите первым. А остальные уже уйдут к другому рефералу. 
Хотя со всех них получите подарок за первый уровень именно Вы.<br>
