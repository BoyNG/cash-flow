Так же в проекте существует система прыжка. Рассмотрим следующий пример.<br>
			<br>
Вам необходимо получить 2-й уровень, но у спонсора 2-го уровня статус уровня 1. 
Т.е. он не может Вам предложить 2-й уровень, так как у него нет его. 
И для того, чтобы Вы не ждали в этом случае, была разработана система "прыжка" через спонсора.
Т.е. система его перепрыгнет и предложит получить 2-й уровень у спонсора, который стоит выше (как на рисунке).
А если вдруг и у него не будет нужного уровня, то система будет перепрыгивать до тех пор, 
пока не найдет Вам спонсора, у которого уровень выше Вашего.<br>
			<br>
Поэтому позаботьтесь о том, чтобы у Вас уже был нужный уровень. 
Чтоб система не перепрыгивала через Вас.<br>
