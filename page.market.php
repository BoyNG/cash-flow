Для равномерного заполнения линий у нас предусмотрена система поочередного распределения переходов рефералов.<br><br>
Допустим, если Ваши рефералы первой линии еще не успели никого пригласить в проект, 
а Вы усиленно продолжаете приводить за собой людей, 
то второй уровень заполнится следующим образом.<br>
			<br>
Сперва рефералы займут места под номерами 4, 7 и 10. 
Затем следующие займут места под номерами 5, 8 и 11. 
А потом оставшиеся 6, 9 и 12. Таким образом рефералы достаются каждому по очереди.
